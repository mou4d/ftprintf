void	ft_putnbr(int nb)
{
	long	l;

	l = nb;
	if (l < 0)
	{
		ft_putchar('-');
		l *= -1;
	}
	if (l > 9)
		ft_putnbr(l / 10);
	ft_putchar((l % 10) + '0');
}
