#include <stddef.h>

void	ft_bzero(void	*s, size_t	n)
{
	size_t			i;
	unsigned char	*p;

	i = 0;
	p = s;
	while (i < n && p[i])
	{
		p[i] = '\0';
		i++;
	}
}
